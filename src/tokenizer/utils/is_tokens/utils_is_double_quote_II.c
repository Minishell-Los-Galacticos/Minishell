#include "../../../../inc/minishell.h"

int	ischrkey(int c)
{
	if (c == '|' || c == '<' || c == '>' || c == '&' || c == '(' || c == ')'
		|| c == ';' || c == '-' || c == '/' || c == '\\' || c == '\0')
		return (0);
	return (1);
}

int	is_escapable_char(char c)
{
	return (c == '\"' || c == '\\' || c == '`' || c == '\n');
}
