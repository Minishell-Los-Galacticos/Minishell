#include "../../../../inc/minishell.h"

int	ischrkey(int c)
{
	if (c == '|' || c == '<' || c == '>' || c == '&' || c == '(' || c == ')'
		|| c == ';' || c == '-' || c == '\0')
		return (0);
	return (1);
}
