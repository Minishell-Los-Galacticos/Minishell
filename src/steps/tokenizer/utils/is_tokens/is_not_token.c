#include "../../../../../inc/minishell.h"

void	is_not_token(const char *str, int *i)
{
	char	c;

	c = str[*i];
	if (c != '|' && c != '>' && c != '<' && c != '"' && c != '\'' && c != '\\'
		&& c != ';' && c != '&' && c != '(' && c != ')' && c != '$' && c != '-'
		&& c != '?' && c != '*'
		&& !ft_isalpha(c) && !ft_isalnum(c))
	{
		(*i)++;
	}
}
