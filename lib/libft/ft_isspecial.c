#include "../libft_plus.h"

int	ft_isspecial(int c)
{
	if (c == '<' || c == '>' || c == '&' || c == '|' || c == '\'' \
		|| c == '?' || c == '\"' || c == '(' || c == ')' || c == '#' \
		|| c == '*' || c == '$' || c == '\\')
		return (1);
	return (0);
}
